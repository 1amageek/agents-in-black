import Foundation

/// GCP Cloud Run deployment provider.
/// Consolidates all Cloud Run-specific logic: preflight checks, URL resolution,
/// config generation, and deployment commands.
public struct GCPCloudRunProvider: DeploymentProvider, Sendable {

    /// Environment variable names reserved by Cloud Run (auto-set by the system).
    private static let reservedCloudRunEnvKeys: Set<String> = [
        "PORT", "K_SERVICE", "K_REVISION", "K_CONFIGURATION",
    ]
    private var reservedCloudRunEnvKeys: Set<String> { Self.reservedCloudRunEnvKeys }

    public var providerID: String { "gcp-cloudrun" }
    public var displayName: String { "Google Cloud Run" }

    public init() {}

    // MARK: - Preflight

    public func preflightCheckers() -> [any PreflightChecker] {
        [
            BuildBackendAvailabilityChecker(),
            GCloudInstalledChecker(),
            GCloudAuthenticatedChecker(),
            GCloudProjectChecker(),
            CloudRunAPIChecker(),
        ]
    }

    public func preflightDependencies() -> [PreflightCheckID: [PreflightCheckID]] {
        [
            .gcloudInstalled: [.gcloudAuthenticated, .gcloudProjectConfigured, .cloudRunAPIEnabled],
        ]
    }

    // MARK: - Config Detection & Validation

    public func extractDetectedConfig(from report: PreflightReport) -> [String: String] {
        var detected: [String: String] = [:]
        if let project = report.detail(for: .gcloudProjectConfigured), !project.isEmpty {
            detected["gcpProject"] = project
        }
        return detected
    }

    public func validateTargetConfig(_ config: AIBDeployTargetConfig) throws {
        guard let gcpProject = config.providerConfig["gcpProject"], !gcpProject.isEmpty else {
            throw AIBDeployError(
                phase: "config",
                message: "GCP project is required. Set it in .aib/targets/gcp-cloudrun.yaml or ensure gcloud is configured."
            )
        }
    }

    // MARK: - Service Naming

    /// Sanitize a package-derived service name into a valid Cloud Run service name.
    /// Cloud Run names: lowercase letters, hyphens, max 63 chars.
    /// Example: "MyService" -> "myservice"
    public func deployedServiceName(from namespacedID: String) -> String {
        let sanitized = namespacedID
            .replacingOccurrences(of: "/", with: "-")
            .lowercased()
            .filter { $0.isLetter || $0.isNumber || $0 == "-" }

        let trimmed: String
        if let first = sanitized.first, first.isLetter {
            trimmed = sanitized
        } else {
            trimmed = "svc-" + sanitized
        }

        return String(trimmed.prefix(63))
    }

    // MARK: - URL Resolution

    /// Resolve a service_ref to a Cloud Run URL.
    /// At plan time the exact hash is unknown, so we use the deterministic form.
    public func resolveURL(
        serviceRef: String,
        region: String,
        path: String?,
        serviceNameMap: [String: String]
    ) -> String {
        let cloudRunName = serviceNameMap[serviceRef] ?? deployedServiceName(from: serviceRef)
        let basePath = path ?? "/mcp"
        return "https://\(cloudRunName)-\(region).a.run.app\(basePath)"
    }

    // MARK: - Deploy Config Generation

    /// Generate clouddeploy.yaml content for a Cloud Run service.
    public func generateDeployConfig(service: AIBDeployServicePlan) -> String {
        var lines: [String] = []
        lines.append("# Generated by AIB — do not edit manually")
        lines.append("service: \(service.deployedServiceName)")
        lines.append("region: \(service.region)")
        lines.append("resources:")
        lines.append("  memory: \(service.resourceConfig.memory)")
        lines.append("  cpu: \(service.resourceConfig.cpu)")
        lines.append("scaling:")
        lines.append("  max_instances: \(service.resourceConfig.maxInstances)")
        lines.append("  min_instances: 0")
        lines.append("  concurrency: \(service.resourceConfig.concurrency)")
        lines.append("timeout: \(service.resourceConfig.timeout)")
        lines.append("auth: \(service.isPublic ? "public" : "private")")

        let planEnvVars = service.envVars.filter { !reservedCloudRunEnvKeys.contains($0.key) }
        if !planEnvVars.isEmpty {
            lines.append("env:")
            for (key, value) in planEnvVars.sorted(by: { $0.key < $1.key }) {
                lines.append("  \(key): \"\(value)\"")
            }
        }

        return lines.joined(separator: "\n") + "\n"
    }

    // MARK: - Container Registry

    public func registryImageTag(
        service: AIBDeployServicePlan,
        targetConfig: AIBDeployTargetConfig
    ) -> String {
        let gcpProject = requiredGCPProject(from: targetConfig)
        let host = targetConfig.providerConfig["artifactRegistryHost"]
            ?? "\(targetConfig.region)-docker.pkg.dev"
        return "\(host)/\(gcpProject)/\(service.deployedServiceName)/service:latest"
    }

    public func registryAuthCommands(targetConfig: AIBDeployTargetConfig) -> [DeployCommand] {
        let host = targetConfig.providerConfig["artifactRegistryHost"]
            ?? "\(targetConfig.region)-docker.pkg.dev"
        let hostForShell = shellQuote(host)
        let script = """
        set -euo pipefail
        if ! command -v container >/dev/null 2>&1; then
          echo "apple/container CLI is not installed. Install it from https://github.com/apple/container/releases"
          exit 127
        fi
        if ! command -v gcloud >/dev/null 2>&1; then
          echo "gcloud CLI is not installed."
          exit 127
        fi
        if container registry list 2>/dev/null | awk 'NR>1 {print $1}' | grep -Fx -- \(hostForShell) >/dev/null 2>&1; then
          echo "container registry login already configured for \(host)"
          exit 0
        fi
        access_token="$(gcloud auth print-access-token)"
        if [ -z "$access_token" ]; then
          echo "Failed to get gcloud access token."
          exit 1
        fi
        printf '%s' "$access_token" | container registry login --username oauth2accesstoken --password-stdin \(hostForShell)
        echo "container registry login completed for \(host)"
        """
        return [
            DeployCommand(
                label: "Configuring apple/container authentication for Artifact Registry",
                arguments: ["bash", "-lc", script],
                stepID: AIBDeployStep.dockerAuth.rawValue
            ),
        ]
    }

    public func buildBackendPreparationCommands(targetConfig: AIBDeployTargetConfig) -> [DeployCommand] {
        let minFreeGiB = max(1, Int(targetConfig.providerConfig["appleContainerMinFreeGiB"] ?? "") ?? 10)
        let minFreeKiB = minFreeGiB * 1024 * 1024
        let script = """
        set -euo pipefail
        if ! command -v container >/dev/null 2>&1; then
          echo "apple/container CLI is not installed. Install it from https://github.com/apple/container/releases"
          exit 127
        fi

        disk_target="/System/Volumes/Data"
        if [ ! -d "$disk_target" ]; then
          disk_target="/"
        fi

        free_kb="$(df -Pk "$disk_target" | awk 'NR==2 {print $4}')"
        case "$free_kb" in
          ''|*[!0-9]*)
            echo "Unable to determine free disk space for $disk_target; skipping cleanup."
            exit 0
            ;;
        esac

        threshold_kb=\(minFreeKiB)
        threshold_gib=\(minFreeGiB)
        free_gib="$((free_kb / 1024 / 1024))"
        echo "Free disk before build prep: ${free_gib}GiB (threshold: ${threshold_gib}GiB)"

        if [ "$free_kb" -gt "$threshold_kb" ]; then
          echo "Disk space is sufficient. Skipping apple/container cleanup."
          exit 0
        fi

        echo "Low disk space detected. Pruning stale apple/container images..."
        set +e
        prune_output="$(printf 'y\\n' | container image prune --all 2>&1)"
        prune_status=$?
        set -e
        if [ -n "$prune_output" ]; then
          echo "$prune_output"
        fi
        if [ "$prune_status" -ne 0 ]; then
          echo "apple/container image prune failed while recovering disk space."
          exit 1
        fi

        if container builder status >/dev/null 2>&1; then
          echo "Resetting apple/container builder cache..."
          set +e
          container builder stop >/dev/null 2>&1
          stop_status=$?
          container builder rm >/dev/null 2>&1
          remove_status=$?
          set -e
          if [ "$stop_status" -ne 0 ] || [ "$remove_status" -ne 0 ]; then
            echo "Warning: builder cache reset reported a non-zero status (stop=$stop_status, rm=$remove_status)."
          fi
        fi

        set +e
        builder_start_output="$(container builder start 2>&1)"
        builder_start_status=$?
        set -e

        if [ "$builder_start_status" -ne 0 ] && echo "$builder_start_output" | grep -qi "default kernel not configured"; then
          echo "Default kernel not configured for this architecture. Installing recommended kernel..."
          set +e
          kernel_setup_output="$(container system kernel set --recommended 2>&1)"
          kernel_setup_status=$?
          set -e
          if [ "$kernel_setup_status" -ne 0 ]; then
            echo "$kernel_setup_output"
            echo "Run: container system kernel set --recommended"
            exit 1
          fi
          set +e
          builder_start_output="$(container builder start 2>&1)"
          builder_start_status=$?
          set -e
        fi

        if [ "$builder_start_status" -ne 0 ]; then
          echo "apple/container builder is not ready after cleanup."
          if [ -n "$builder_start_output" ]; then
            echo "$builder_start_output"
          fi
          echo "Run: container system start && container builder start"
          exit 1
        fi

        free_kb_after="$(df -Pk "$disk_target" | awk 'NR==2 {print $4}')"
        case "$free_kb_after" in
          ''|*[!0-9]*)
            echo "Cleanup completed, but failed to re-check free disk space."
            exit 0
            ;;
        esac
        free_gib_after="$((free_kb_after / 1024 / 1024))"
        echo "Free disk after build prep: ${free_gib_after}GiB"

        if [ "$free_kb_after" -lt "$threshold_kb" ]; then
          echo "Disk space is still below ${threshold_gib}GiB after cleanup. Free more disk and retry."
          exit 1
        fi
        """
        return [
            DeployCommand(
                label: "Preparing apple/container build environment",
                arguments: ["bash", "-lc", script],
                stepID: AIBDeployStep.dockerBuild.rawValue
            ),
        ]
    }

    public func ensureRegistryRepoCommands(
        service: AIBDeployServicePlan,
        targetConfig: AIBDeployTargetConfig
    ) -> [DeployCommand] {
        let gcpProject = requiredGCPProject(from: targetConfig)
        let repoName = service.deployedServiceName
        let describeCommand = [
            "gcloud", "artifacts", "repositories", "describe",
            repoName,
            "--location=\(targetConfig.region)",
            "--project=\(gcpProject)",
            "--format='value(name)'",
        ].joined(separator: " ")
        let createCommand = [
            "gcloud", "artifacts", "repositories", "create",
            repoName,
            "--repository-format=docker",
            "--location=\(targetConfig.region)",
            "--project=\(gcpProject)",
            "--quiet",
        ].joined(separator: " ")
        let script = """
        set -euo pipefail
        if \(describeCommand) >/dev/null 2>&1; then
          echo "Repository [\(repoName)] already exists."
        else
          \(createCommand)
        fi
        """
        return [
            DeployCommand(
                label: "Ensuring Artifact Registry repository for \(service.deployedServiceName)",
                arguments: ["bash", "-lc", script],
                stepID: AIBDeployStep.registrySetup.rawValue
            ),
        ]
    }

    // MARK: - Build & Push Commands

    public func buildAndPushCommands(
        imageTag: String,
        dockerfilePath: String,
        buildContext: String,
        targetConfig: AIBDeployTargetConfig
    ) -> [DeployCommand] {
        let stagedContextEnabled = parseTruthyBool(targetConfig.providerConfig["appleContainerStageContext"])
        let dockerfileArgument: String
        if dockerfilePath.hasPrefix(buildContext + "/") {
            dockerfileArgument = String(dockerfilePath.dropFirst(buildContext.count + 1))
        } else {
            dockerfileArgument = dockerfilePath
        }
        let contextForShell = shellQuote(buildContext)
        let dockerfileForShell = shellQuote(dockerfileArgument)
        let imageForShell = shellQuote(imageTag)
        let imageHost = imageTag.split(separator: "/", maxSplits: 1).first.map(String.init) ?? imageTag
        let imageHostForShell = shellQuote(imageHost)
        let script = """
        set -euo pipefail
        if ! command -v container >/dev/null 2>&1; then
          echo "apple/container CLI is not installed. Install it from https://github.com/apple/container/releases"
          exit 127
        fi
        if ! command -v gcloud >/dev/null 2>&1; then
          echo "gcloud CLI is not installed."
          exit 127
        fi
        refresh_registry_auth() {
          access_token="$(gcloud auth print-access-token)"
          if [ -z "$access_token" ]; then
            echo "Failed to get gcloud access token."
            return 1
          fi
          printf '%s' "$access_token" | container registry login --username oauth2accesstoken --password-stdin \(imageHostForShell)
        }
        if ! container builder status >/dev/null 2>&1; then
          set +e
          builder_start_output="$(container builder start 2>&1)"
          builder_start_status=$?
          set -e

          if [ "$builder_start_status" -ne 0 ] && echo "$builder_start_output" | grep -qi "default kernel not configured"; then
            echo "Default kernel not configured for this architecture. Installing recommended kernel..."
            set +e
            kernel_setup_output="$(container system kernel set --recommended 2>&1)"
            kernel_setup_status=$?
            set -e
            if [ "$kernel_setup_status" -ne 0 ]; then
              echo "$kernel_setup_output"
              echo "Run: container system kernel set --recommended"
              exit 1
            fi
            set +e
            builder_start_output="$(container builder start 2>&1)"
            builder_start_status=$?
            set -e
          fi

          if [ "$builder_start_status" -ne 0 ]; then
            echo "apple/container builder is not ready."
            if [ -n "$builder_start_output" ]; then
              echo "$builder_start_output"
            fi
            echo "Run: container system start && container builder start"
            exit 1
          fi

          if ! container builder status >/dev/null 2>&1; then
            echo "apple/container builder did not reach running state."
            exit 1
          fi
        fi
        echo "Build backend: apple-container"
        build_start="$(date +%s)"
        cd \(contextForShell)
        stage_context="."
        stage_dir=""
        cleanup_staged_context() {
          if [ -n "${stage_dir:-}" ] && [ -d "$stage_dir" ]; then
            rm -rf "$stage_dir"
            stage_dir=""
          fi
        }
        if [ "\(stagedContextEnabled ? "1" : "0")" = "1" ] && [ -f .dockerignore ]; then
          if grep -Eq '^[[:space:]]*!' .dockerignore; then
            echo ".dockerignore contains negation patterns; skipping staged build context optimization."
          elif command -v rsync >/dev/null 2>&1; then
            stage_sync_start="$(date +%s)"
            stage_dir="$(mktemp -d -t aib-container-context.XXXXXX)"
            set +e
            rsync -a --delete --exclude-from=.dockerignore ./ "$stage_dir"/
            stage_sync_status=$?
            set -e
            if [ "$stage_sync_status" -eq 0 ]; then
              stage_context="$stage_dir"
              stage_sync_end="$(date +%s)"
              echo "Prepared staged build context in $((stage_sync_end - stage_sync_start))s"
            else
              echo "Failed to stage build context with rsync (exit $stage_sync_status). Falling back to direct build context."
              cleanup_staged_context
            fi
          else
            echo "rsync not found; skipping staged build context optimization."
          fi
        fi
        build_log="$(mktemp -t aib-container-build.XXXXXX)"
        build_attempt=1
        build_max_attempts=2
        staged_context_fallback_attempted=0
        while true; do
          set +e
          container build --platform linux/amd64 --file \(dockerfileForShell) --tag \(imageForShell) "$stage_context" 2>&1 | tee "$build_log"
          build_status=${PIPESTATUS[0]}
          set -e
          if [ "$build_status" -eq 0 ]; then
            break
          fi

          if [ "$stage_context" != "." ] && [ "$staged_context_fallback_attempted" -eq 0 ]; then
            echo "Staged build context may be incompatible with this service. Retrying with direct build context..."
            cleanup_staged_context
            stage_context="."
            staged_context_fallback_attempted=1
            continue
          fi

          if grep -Eqi "ECONNRESET|npm error network aborted|connection reset by peer|TLS handshake timeout|i/o timeout|temporary failure" "$build_log" \
            && [ "$build_attempt" -lt "$build_max_attempts" ]; then
            retry_delay=$((build_attempt * 5))
            next_attempt=$((build_attempt + 1))
            echo "Transient network failure detected during image build. Retrying in ${retry_delay}s (attempt ${next_attempt}/${build_max_attempts})..."
            sleep "$retry_delay"
            build_attempt="$next_attempt"
            continue
          fi

          rm -f "$build_log"
          cleanup_staged_context
          exit "$build_status"
        done
        rm -f "$build_log"
        build_end="$(date +%s)"
        echo "apple/container build duration: $((build_end - build_start))s"
        push_start="$(date +%s)"
        push_log="$(mktemp -t aib-container-push.XXXXXX)"
        push_attempt=1
        push_max_attempts=2
        push_auth_refresh_attempted=0
        while true; do
          set +e
          container image push --platform linux/amd64 \(imageForShell) 2>&1 | tee "$push_log"
          push_status=${PIPESTATUS[0]}
          set -e
          if [ "$push_status" -eq 0 ]; then
            break
          fi

          if grep -Eqi "unauthorized|authentication required|denied|no basic auth credentials|insufficient scope" "$push_log" \
            && [ "$push_auth_refresh_attempted" -eq 0 ]; then
            echo "Registry authentication appears stale. Refreshing login and retrying push..."
            refresh_registry_auth
            push_auth_refresh_attempted=1
            continue
          fi

          if grep -Eqi "connection reset by peer|TLS handshake timeout|i/o timeout|temporary failure|timeout" "$push_log" \
            && [ "$push_attempt" -lt "$push_max_attempts" ]; then
            retry_delay=$((push_attempt * 5))
            next_attempt=$((push_attempt + 1))
            echo "Transient network failure detected during image push. Retrying in ${retry_delay}s (attempt ${next_attempt}/${push_max_attempts})..."
            sleep "$retry_delay"
            push_attempt="$next_attempt"
            continue
          fi

          rm -f "$push_log"
          cleanup_staged_context
          exit "$push_status"
        done
        rm -f "$push_log"
        push_end="$(date +%s)"
        echo "apple/container push duration: $((push_end - push_start))s"
        cleanup_staged_context
        """

        return [
            DeployCommand(
                label: "Building and pushing image",
                arguments: ["bash", "-lc", script],
                stepID: AIBDeployStep.dockerBuild.rawValue
            ),
            DeployCommand(
                label: "Image push completed",
                arguments: ["bash", "-lc", "echo \"Image pushed \(imageTag)\""],
                stepID: AIBDeployStep.dockerPush.rawValue
            ),
        ]
    }

    // MARK: - Deploy Commands

    public func deployCommands(
        service: AIBDeployServicePlan,
        imageTag: String,
        targetConfig: AIBDeployTargetConfig,
        secrets: [String: String] = [:]
    ) -> [DeployCommand] {
        let gcpProject = requiredGCPProject(from: targetConfig)

        var args = [
            "gcloud", "run", "deploy", service.deployedServiceName,
            "--image", imageTag,
            "--region", service.region,
            "--memory", service.resourceConfig.memory,
            "--cpu", service.resourceConfig.cpu,
            "--max-instances", String(service.resourceConfig.maxInstances),
            "--concurrency", String(service.resourceConfig.concurrency),
            "--timeout", service.resourceConfig.timeout,
            "--project", gcpProject,
            "--format", "value(status.url)",
            "--quiet",
        ]

        if service.isPublic {
            args.append("--allow-unauthenticated")
        } else {
            args.append("--no-allow-unauthenticated")
        }

        // Merge regular env vars + secrets into a single --set-env-vars.
        // Use gcloud's ^||^ custom delimiter to avoid values containing commas
        // or other special characters from breaking the parser.
        var allEnvVars = service.envVars.filter { !reservedCloudRunEnvKeys.contains($0.key) }
        for (key, value) in secrets {
            allEnvVars[key] = value
        }

        if !allEnvVars.isEmpty {
            let delimiter = "||"
            let envString = "^" + delimiter + "^"
                + allEnvVars
                .sorted(by: { $0.key < $1.key })
                .map { "\($0.key)=\($0.value)" }
                .joined(separator: delimiter)
            args.append(contentsOf: ["--set-env-vars", envString])
        }

        return [
            DeployCommand(
                label: "Deploying to Cloud Run",
                arguments: args,
                stepID: AIBDeployStep.serviceDeploy.rawValue
            ),
        ]
    }

    // MARK: - Auth Binding Commands

    public func authBindingCommands(
        binding: AIBDeployAuthBinding,
        targetConfig: AIBDeployTargetConfig
    ) -> [DeployCommand] {
        let gcpProject = requiredGCPProject(from: targetConfig)
        let sourceForShell = shellQuote(binding.sourceServiceName)
        let targetForShell = shellQuote(binding.targetServiceName)
        let roleForShell = shellQuote(binding.role)
        let regionForShell = shellQuote(targetConfig.region)
        let projectForShell = shellQuote(gcpProject)
        let script = """
        set -euo pipefail
        source_service=\(sourceForShell)
        target_service=\(targetForShell)
        role=\(roleForShell)
        region=\(regionForShell)
        project=\(projectForShell)

        source_sa="$(gcloud run services describe "$source_service" --region "$region" --project "$project" --format='value(spec.template.spec.serviceAccountName)' 2>/dev/null | tr -d '[:space:]')"
        if [ -z "$source_sa" ]; then
          project_number="$(gcloud projects describe "$project" --format='value(projectNumber)' | tr -d '[:space:]')"
          if [ -z "$project_number" ]; then
            echo "Failed to resolve project number for $project."
            exit 1
          fi
          source_sa="${project_number}-compute@developer.gserviceaccount.com"
        fi

        if [ "${source_sa#*@}" = "$source_sa" ]; then
          source_sa="${source_sa}@${project}.iam.gserviceaccount.com"
        fi

        if ! gcloud iam service-accounts describe "$source_sa" --project "$project" >/dev/null 2>&1; then
          echo "Service account $source_sa does not exist."
          exit 1
        fi

        member="serviceAccount:${source_sa}"
        echo "Using invoker principal: ${member}"

        gcloud run services add-iam-policy-binding "$target_service" \
          --member "$member" \
          --role "$role" \
          --region "$region" \
          --project "$project" \
          --quiet
        """

        return [
            DeployCommand(
                label: "Binding IAM: \(binding.sourceServiceName) → \(binding.targetServiceName)",
                arguments: ["bash", "-lc", script],
                stepID: AIBDeployStep.authBind.rawValue
            ),
        ]
    }

    // MARK: - Existing Env Vars

    public func existingEnvVarNames(
        serviceName: String,
        targetConfig: AIBDeployTargetConfig
    ) async -> Set<String> {
        let gcpProject = targetConfig.providerConfig["gcpProject"] ?? ""
        guard !gcpProject.isEmpty else { return [] }

        // Use JSON output for reliable parsing
        let command = "gcloud run services describe \(serviceName)"
            + " --region=\(targetConfig.region)"
            + " --project=\(gcpProject)"
            + " --format='json(spec.template.spec.containers[0].env)'"

        do {
            let result = try await ShellProbe.run(command: command, timeout: .seconds(15))
            guard result.exitCode == 0, !result.stdout.isEmpty else { return [] }

            guard let data = result.stdout.data(using: .utf8) else { return [] }
            let jsonObject: Any
            do {
                jsonObject = try JSONSerialization.jsonObject(with: data)
            } catch {
                return []
            }

            let envEntries: [[String: Any]]
            if let topLevelArray = jsonObject as? [[String: Any]] {
                // `--format='json(spec.template.spec.containers[0].env)'` returns an array.
                envEntries = topLevelArray
            } else if let topLevelDict = jsonObject as? [String: Any],
                      let envArray = Self.extractEnvArray(from: topLevelDict)
            {
                // Fallback for full-service JSON output shape.
                envEntries = envArray
            } else {
                return []
            }

            var names: Set<String> = []
            for entry in envEntries {
                if let name = entry["name"] as? String, !name.isEmpty {
                    names.insert(name)
                }
            }
            return names
        } catch {
            return []
        }
    }

    // MARK: - Parse Output

    public func parseDeployedURL(from output: String) -> String? {
        let trimmed = output.trimmingCharacters(in: .whitespacesAndNewlines)
        guard !trimmed.isEmpty else { return nil }
        return trimmed
    }

    // MARK: - Auth Binding Member

    public func authBindingMember(
        sourceServiceName: String,
        targetConfig: AIBDeployTargetConfig
    ) -> String {
        let gcpProject = requiredGCPProject(from: targetConfig)
        return "serviceAccount:\(sourceServiceName)@\(gcpProject).iam.gserviceaccount.com"
    }

    // MARK: - Private

    /// Extract gcpProject from validated config.
    /// Precondition: `validateTargetConfig()` must have been called before any method that uses this.
    private func requiredGCPProject(from targetConfig: AIBDeployTargetConfig) -> String {
        guard let project = targetConfig.providerConfig["gcpProject"], !project.isEmpty else {
            preconditionFailure(
                "gcpProject is missing from targetConfig. "
                + "validateTargetConfig() must be called before deploy/auth operations."
            )
        }
        return project
    }

    private func shellQuote(_ value: String) -> String {
        "'\(value.replacingOccurrences(of: "'", with: "'\"'\"'"))'"
    }

    private func parseTruthyBool(_ raw: String?) -> Bool {
        guard let value = raw?.trimmingCharacters(in: .whitespacesAndNewlines).lowercased() else {
            return false
        }
        switch value {
        case "1", "true", "yes", "on":
            return true
        default:
            return false
        }
    }

    private static func extractEnvArray(from json: [String: Any]) -> [[String: Any]]? {
        if let env = json["env"] as? [[String: Any]] {
            return env
        }
        if let spec = json["spec"] as? [String: Any],
           let template = spec["template"] as? [String: Any],
           let templateSpec = template["spec"] as? [String: Any],
           let containers = templateSpec["containers"] as? [[String: Any]],
           let first = containers.first,
           let env = first["env"] as? [[String: Any]]
        {
            return env
        }
        return nil
    }

